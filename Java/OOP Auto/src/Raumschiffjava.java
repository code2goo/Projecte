public class Raumschiffjava {

        double geschwiendigkeit;
        double leerGewischt;
        double maxGewischt;
        String farbe;
        double grosse;

        public void setGeschwiendigkeit(double g){

            geschwiendigkeit = g;
        }
        public double getGeschwiendigkeit(){

            return geschwiendigkeit;
        }

        public void setLeerGewischt(double l){
            leerGewischt = l;
        }
        public double getLeerGewischt(){

            return leerGewischt;
        }
        public void setMaxGewischt(double m){

            maxGewischt = m;
        }
        public double getMaxGewischt(){

            return maxGewischt;
        }
        public void setGrosse(double g){

            grosse = g;
        }
        public double getGrosse(){

            return grosse;
        }
        public void setFarbe(String f){

            farbe = f;
        }
        public String getFarbe(){

            return farbe;
        }

}
